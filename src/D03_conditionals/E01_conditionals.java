package D03_conditionals;

public class E01_conditionals {
    public static void main(String[] args) {
        int sayi = 24;
        if (sayi < 20) {
            System.out.println("sayi 20 den kücüktür..");
        } else if (sayi == 20) {
            System.out.println("sayi 20 ye esittir. ");
        } else {
            System.out.println("sayi yirmiden büyüktür.");
        }
    }
}
