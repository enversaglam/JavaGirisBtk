package D04_Loops;

public class E01_loopDemo {
    public static void main(String[] args) {

        //For döngüsü

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
        System.out.println("loop finish..");

        for (int i = 2; i < 10; i+=2) {
            System.out.println(i);
        }
        System.out.println("loop 2 finish..");
    }
}
